
public class rqq {
	
	    public static void main(String[] args) {
	        // 숫자를 담을 수 있는 상자(배열)를 만듭니다.
	        int[] numbers = {1, 2, 3, 4, 5};
	        
	        // 배열의 길이를 출력합니다.
	        System.out.println("배열의 길이: " + numbers.length);
	        
	        // 배열에 들어있는 모든 숫자를 출력합니다.
	        System.out.print("배열 안에 있는 숫자: ");
	        for (int i = 0;  i <= 10; i++) {
	            System.out.print(numbers[i] + " ,");
	        }

	    }
	}


